class Figuras{

    private  int noVertices, noLados;

 public Figuras(int noVertices, int noLados){
     this.noVertices = noVertices;
     this.noLados = noLados;

 }

 public int getNoVertices() {
     return noVertices;
 }

 public void setNoVertices(int noVertices) {
     this.noVertices = noVertices;
 }

 public int getNoLados() {
     return noLados;
 }

 public void setNoLados(int noLados) {
     this.noLados = noLados;
 }

}